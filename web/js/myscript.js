$(document).ready(function () {

        $("#action").mouseenter(
            function () {
                $("#reaction").slideDown('medium');
            }
        );


    }
);

$(document).ready(function () {

        $("#reaction").mouseleave(
            function () {
                $("#reaction").slideUp('medium');
            }
        );


    }
);

$(document).ready(function () {

        $("#navigation").mouseleave(
            function () {
                $("#reaction").slideUp('medium');
            }
        );


    }
);
